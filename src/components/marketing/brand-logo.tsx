interface BrandLogoProps {
  name: string;
  slug?: keyof typeof BRAND_ICONS;
  className?: string;
}

/**
 * Renders a real brand mark from Simple Icons when one is available
 * (WhatsApp, SAP, Odoo, Shopee). OpenAI and AWS have real logos too, but as
 * static files under public/marketing/logos (see partners-section.tsx) since
 * their official marks are multi-color or a non-square viewBox that doesn't
 * fit this single-path/single-hex shape. Several other brands (Microsoft,
 * Oracle, Lazada, Amazon) aren't available as a real vector asset at all -
 * those fall back to a plain typographic wordmark instead of a hand-drawn
 * approximation of their logo.
 */
export function BrandLogo({ name, slug, className }: BrandLogoProps) {
  const icon = slug ? BRAND_ICONS[slug] : undefined;

  if (icon) {
    return (
      <svg
        role="img"
        viewBox="0 0 24 24"
        className={className}
        fill={`#${icon.hex}`}
        aria-label={name}
      >
        <path d={icon.path} />
      </svg>
    );
  }

  return (
    <span className={`flex items-center justify-center font-semibold tracking-tight text-owly-text-light ${className || ""}`}>
      {name}
    </span>
  );
}
